//  if statement if true

if(true){
    console.log('this is true');
}

//  if statement if false
if(false){
    console.log('this is not false')
}

//  if statement with conditions
let x = 10;
let y = 5;

if(x >= 10){
    console.log(`${x} is greater than or equals to ${y}`);
}

//  if statement with logic
if(7 === 7){
    console.log('Yes it is equals to 7');
}

//  if statement with else
if(x < y){
    console.log(`${x} is lesser than or equals to ${y}`);
} else {
    console.log(`${x} is lesser than or equals to ${y}`);
}